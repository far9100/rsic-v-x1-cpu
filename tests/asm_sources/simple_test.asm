# 極簡測試 - 只測試第一個store指令的前遞問題
# 僅包含3條指令

_start:
    # 設置 x1 = 5
    addi x1, x0, 5          # x1 = 5
    
    # 設置結果地址 x2 = 0x200
    addi x2, x0, 512        # x2 = 0x200
    
    # 立即使用x1和x2 (測試前遞)
    sw x1, 0(x2)            # 儲存 x1(5) 到 地址 x2+0 (0x200)
    
    # 結束程序
    addi x3, x0, 1          # x3 = 1
    addi x4, x0, 768        # x4 = 0x300
    sw x3, 0(x4)            # 儲存結束標記 1 到 0x300
    
    # 無限循環
    beq x0, x0, _start 